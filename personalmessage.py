"""use a variable to represent a person's name , and print a 
message to that person"""

first_name = "salma"
last_name = "hayeck"
full_name = (f" {first_name} {last_name}")
print (f"Hello {full_name.title()} te gustaría aprender python") #el método title convierte las iniciales en mayusc
print (f"Hello {full_name.upper()} te gustaría aprender python") #todas mayusc
print (f"Hello {full_name.lower()} te gustaría aprender python") #todas minusc
